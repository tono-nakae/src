#include <machine/asm.h>

#include "abi.h"


RCSID("$NetBSD: e_fmod.S,v 1.5 2001/06/19 00:26:30 fvdl Exp $")

ENTRY(__ieee754_fmod)
	XMM_TWO_ARG_DOUBLE_PROLOGUE
	fldl	ARG_DOUBLE_TWO
	fldl	ARG_DOUBLE_ONE
1:	fprem
	fstsw	%ax
	btw	$2,%ax
	jc	1b
	fstp	%st(1)
	XMM_DOUBLE_EPILOGUE
	ret
